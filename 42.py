"""| Method                          | Description                                    |
| ------------------------------- | ---------------------------------------------- |
| `dict.keys()`                   | Returns all keys                               |
| `dict.values()`                 | Returns all values                             |
| `dict.items()`                  | Returns key-value pairs                        |
| `dict.update({...})`            | Updates dictionary                             |
| `dict.copy()`                   | Copies dictionary                              |
| `dict.fromkeys(keys, value)`    | Creates dictionary from keys                   |
| `dict.setdefault(key, default)` | Returns value if key exists, else adds default |
"""